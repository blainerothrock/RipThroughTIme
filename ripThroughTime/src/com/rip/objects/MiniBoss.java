package com.rip.objects;

import com.badlogic.gdx.graphics.Texture;

public abstract class MiniBoss extends Enemy {

	public MiniBoss(int x, int y, float width, float height,
			Texture texture, int SPEED) {
		super(x, y, width, height, texture, SPEED, 45);

		this.health = 60;
		this.damage = 20;
	}
	
	public MiniBoss(int x, int y, float width, float height, int SPEED) {
		super(x, y, width, height, SPEED, 45);
		
		this.health = 60;
		this.damage = 20;
	}

}
